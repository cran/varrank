##-------------------------------------------------------------------------
## Function that computes skewness of a distribution
##-------------------------------------------------------------------------

skewness <- function(x){
  n <- length(x)
  (sum((x-mean(x))^3)/n)/(sum((x-mean(x))^2)/n)^(3/2)
}


